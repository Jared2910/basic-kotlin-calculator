package design.patterns.basic.calculator

internal class Calculator() {
    fun suma(numberA: Int, numberB: Int) = numberA + numberB

    fun resta(numberA: Int, numberB: Int) = numberA - numberB

    fun multiplicacion(numberA: Int, numberB: Int) = numberA * numberB

    fun division(numberA: Int, numberB: Int) = numberA / numberB


}
